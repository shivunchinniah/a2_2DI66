            "def plot_queue_progress_ci(df, title):\n",
            "    time_bins = np.arange(0, 35000, 60)\n",
            "    time_hours = 9.5 + (time_bins / 3600.0) \n",
            "    \n",
            "    locations = [loc for loc in df['location'].unique() if loc != 'EXIT']\n",
            "    records = []\n",
            "    \n",
            "    for rep in df['replication'].unique():\n",
            "        rep_df = df[df['replication'] == rep]\n",
            "        for loc in locations:\n",
            "            loc_df = rep_df[(rep_df['location'] == loc) & (rep_df['start_time'] < rep_df['end_time'])]\n",
            "            small_df = loc_df[loc_df['vehicle_size'] == 'SMALL']\n",
            "            big_df = loc_df[loc_df['vehicle_size'] == 'BIG']\n",
            "            \n",
            "            b_weight = 2 if loc not in ['MAIN_QUEUE', 'HALL_QUEUE'] else 1\n",
            "            \n",
            "            s_occ = np.zeros(len(time_bins))\n",
            "            if not small_df.empty:\n",
            "                s_arr = pd.DataFrame({'time': small_df['start_time'], 'change': 1})\n",
            "                s_dep = pd.DataFrame({'time': small_df['end_time'], 'change': -1})\n",
            "                s_ev = pd.concat([s_arr, s_dep]).sort_values(by=['time', 'change'], ascending=[True, False])\n",
            "                s_ev['occupancy'] = s_ev['change'].cumsum()\n",
            "                s_idxs = np.searchsorted(s_ev['time'].values, time_bins, side='right') - 1\n",
            "                s_occ = np.where(s_idxs >= 0, s_ev['occupancy'].values[s_idxs], 0)\n",
            "                \n",
            "            b_occ = np.zeros(len(time_bins))\n",
            "            if not big_df.empty:\n",
            "                b_arr = pd.DataFrame({'time': big_df['start_time'], 'change': b_weight})\n",
            "                b_dep = pd.DataFrame({'time': big_df['end_time'], 'change': -b_weight})\n",
            "                b_ev = pd.concat([b_arr, b_dep]).sort_values(by=['time', 'change'], ascending=[True, False])\n",
            "                b_ev['occupancy'] = b_ev['change'].cumsum()\n",
            "                b_idxs = np.searchsorted(b_ev['time'].values, time_bins, side='right') - 1\n",
            "                b_occ = np.where(b_idxs >= 0, b_ev['occupancy'].values[b_idxs], 0)\n",
            "                \n",
            "            tot_occ = s_occ + b_occ\n",
            "            \n",
            "            temp_tot = pd.DataFrame({'Time of Day (Hours)': time_hours, 'Occupancy': tot_occ, 'Type': 'Total Capacity Used'})\n",
            "            temp_tot['location'] = loc\n",
            "            temp_tot['replication'] = rep\n",
            "            records.append(temp_tot)\n",
            "            \n",
            "            temp_big = pd.DataFrame({'Time of Day (Hours)': time_hours, 'Occupancy': b_occ, 'Type': 'Capacity Used (BIG Cars)'})\n",
            "            temp_big['location'] = loc\n",
            "            temp_big['replication'] = rep\n",
            "            records.append(temp_big)\n",
            "            \n",
            "    progress_df = pd.concat(records, ignore_index=True)\n",
            "    \n",
            "    max_cap = {\n",
            "        'MAIN_QUEUE': None,\n",
            "        'HALL_QUEUE': 14,\n",
            "        'HALL_OVERFLOW': 12,\n",
            "        'DCDD': 7,\n",
            "        'GREEN': 5,\n",
            "        'REST': 5\n",
