            "def run_replications(scale=1.0, num_replications=30):\n",
            "    all_traces = []\n",
            "    all_turnarounds = []\n",
            "    \n",
            "    for rep in range(num_replications):\n",
            "        customers = generate_scheduled_customers(scale)\n",
            "        env = setup_environment(customers)\n",
            "        \n",
            "        # Run up to 35000s (~9.7 hours) to ensure all customers finish\n",
            "        env.run(end_time=35000)\n",
            "        \n",
            "        data = []\n",
            "        for i, c in enumerate(customers):\n",
            "            cid = getattr(c, 'id', i) \n",
            "            for item in c.itinerary:\n",
            "                data.append({\n",
            "                    'replication': rep,\n",
            "                    'customer_id': cid,\n",
            "                    'vehicle_size': c.vehicle_size.name,\n",
            "                    'location': item.location.name,\n",
            "                    'start_time': item.start_time,\n",
            "                    'end_time': item.end_time,\n",
            "                    'wait_time': item.time_waiting,\n",
            "                    'service_time': item.service_time\n",
            "                })\n",
            "                \n",
            "        df_rep = pd.DataFrame(data)\n",
            "        df_rep['queue_exit_time'] = df_rep['start_time'] + df_rep['wait_time']\n",
            "        all_traces.append(df_rep)\n",
            "        \n",
            "        sys_time = df_rep.groupby('customer_id').agg(\n",
            "            entry_time=('start_time', 'min'),\n",
            "            exit_time=('end_time', 'max')\n",
            "        )\n",
            "        sys_time['total_time'] = sys_time['exit_time'] - sys_time['entry_time']\n",
            "        sys_time['replication'] = rep\n",
            "        all_turnarounds.append(sys_time)\n",
            "        \n",
            "    return pd.concat(all_traces, ignore_index=True), pd.concat(all_turnarounds, ignore_index=True)\n",
            "\n",
            "print(\"Running 100 Monte Carlo Replications for Baseline Scheduled Arrivals...\")\n",
            "df_base, turn_base = run_replications(scale=1.0, num_replications=100)\n",
