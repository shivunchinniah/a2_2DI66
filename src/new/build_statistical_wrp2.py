            "class Sampler:\n",
            "    def __init__(self, name, params, seed=None):\n",
            "        self.rng = np.random.default_rng(seed)\n",
            "        self._sampling_method = getattr(self.rng, name)\n",
            "        self.dist_params = params\n",
            "\n",
            "    def sample(self, size=None):\n",
            "        val = self._sampling_method(**self.dist_params, size=size)\n",
            "        return max(0.1, abs(float(np.squeeze(val))))\n",
            "\n",
            "def get_gamma_params(mean, stdev):\n",
            "    variance = stdev ** 2\n",
            "    shape = (mean ** 2) / variance\n",
            "    scale = variance / mean\n",
            "    return {\"shape\": shape, \"scale\": scale}\n",
            "\n",
            "# Data extracted from \"Data Lodewijkstraat.xlsx\"\n",
            "samplers = {\n",
            "    'entrance': Sampler('gamma', get_gamma_params(30, 12)),\n",
            "    'hall_big': Sampler('gamma', get_gamma_params(423, 270)),\n",
            "    'hall_small': Sampler('gamma', get_gamma_params(240, 150)),\n",
