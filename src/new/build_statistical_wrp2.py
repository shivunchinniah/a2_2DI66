            "def generate_scheduled_customers(scale=1.0):\n",
            "    generator = CustomerItineraryGenerator()\n",
            "    dse_customers = []\n",
            "    \n",
            "    location_map = {\n",
            "        0: LocationType.MAIN_QUEUE, \n",
            "        1: LocationType.HALL_OVERFLOW, \n",
            "        2: LocationType.DCDD, \n",
            "        3: LocationType.GREEN,\n",
            "        4: LocationType.REST, \n",
            "        5: LocationType.EXIT\n",
            "    }\n",
            "\n",
            "    # Defined schedule: (start_sec, end_sec, rate_per_hour)\n",
            "    # 0 sec = 09:30 AM\n",
            "    schedule = [\n",
            "        (0,      1800,  10 * scale),\n",
            "        (1800,   4500,  100 * scale),\n",
            "        (4500,   7200,  40 * scale),\n",
            "        (7200,   12600, 100 * scale),\n",
            "        (12600,  19800, 100 * scale),\n",
            "        (19800,  25200, 80 * scale),\n",
            "        (25200,  27000, 50 * scale)\n",
            "    ]\n",
            "    \n",
            "    arrival_clock = 0.0\n",
            "    rng = np.random.default_rng()\n",
            "    \n",
            "    for start_time, end_time, rate_per_hour in schedule:\n",
            "        if rate_per_hour <= 0: continue\n",
            "        rate_per_sec = rate_per_hour / 3600.0\n",
            "        \n",
            "        if arrival_clock < start_time:\n",
            "            arrival_clock = start_time\n",
            "            \n",
            "        while True:\n",
            "            inter_arrival = rng.exponential(1.0 / rate_per_sec)\n",
            "            arrival_clock += inter_arrival\n",
            "            \n",
            "            if arrival_clock >= end_time:\n",
            "                # Rewind clock slightly to boundary so next bucket properly captures overflow\n",
            "                arrival_clock -= inter_arrival\n",
            "                break\n",
            "                \n",
            "            raw_itinerary = generator.Next()\n",
            "            v_size = VehicleSize.BIG if np.random.rand() < 0.47 else VehicleSize.SMALL\n",
            "            \n",
            "            dse_itinerary = []\n",
            "            for i, loc_int in enumerate(raw_itinerary):\n",
            "                loc_enum = location_map[loc_int]\n",
            "                \n",
            "                service_time = 0.0\n",
            "                if loc_enum == LocationType.HALL_OVERFLOW:\n",
            "                    service_time = samplers['hall_big'].sample() if v_size == VehicleSize.BIG else samplers['hall_small'].sample()\n",
            "                elif loc_enum == LocationType.DCDD:\n",
            "                    service_time = samplers['dcdd'].sample()\n",
            "                elif loc_enum == LocationType.GREEN:\n",
            "                    service_time = samplers['green'].sample()\n",
            "                elif loc_enum == LocationType.REST:\n",
            "                    service_time = samplers['rest'].sample()\n",
            "                    \n",
            "                item = ItineraryItem(location=loc_enum, service_time=service_time)\n",
            "                if i == 0:\n",
            "                    item.start_time = arrival_clock\n",
            "                dse_itinerary.append(item)\n",
            "                if i == 0:\n",
            "                    dse_itinerary.append(ItineraryItem(location=LocationType.HALL_QUEUE, service_time=0))\n",
            "                    \n",
            "            import DSE \n",
            "            dse_customers.append(DSE.Customer(dse_itinerary, v_size))\n",
            "            \n",
            "    return dse_customers\n"
